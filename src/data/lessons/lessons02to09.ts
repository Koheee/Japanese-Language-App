import { DialogueTurn, Lesson, VocabularyItem } from '../../models/content';

type VocabularySeed = readonly [japanese: string, reading: string, english: string, partOfSpeech: string, note?: string];

const vocabulary = (lesson: number, items: readonly VocabularySeed[]): VocabularyItem[] =>
  items.map(([japanese, reading, english, partOfSpeech, note], index) => ({
    id: `l${lesson}-v${String(index + 1).padStart(2, '0')}`,
    japanese,
    reading,
    english,
    partOfSpeech,
    ...(note ? { note } : {}),
  }));

type DialogueSeed = readonly [speaker: string, japanese: string, reading: string, english: string, grammarIds?: string[]];

const dialogue = (lesson: number, turns: readonly DialogueSeed[]): DialogueTurn[] =>
  turns.map(([speaker, japanese, reading, english, grammarIds], index) => ({
    id: `l${lesson}-d${String(index + 1).padStart(2, '0')}`,
    speaker,
    japanese,
    reading,
    english,
    ...(grammarIds ? { grammarIds } : {}),
  }));

